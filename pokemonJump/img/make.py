from PIL import Image

# cut下的小图片的大小
x = 80
y = 80

img1 = Image.open("jng1.png")
cut1 = img1.crop((48*2,48*2,48*3,48*3))
cut2 = img1.crop((48*0,48*1,48*1,48*2))
cut3 = img1.crop((48*3,48*2,48*4,48*3))
cut4 = img1.crop((48*1,48*1,48*2,48*2))
resizeCut1 = cut1.resize((x,y),Image.ANTIALIAS)
resizeCut2 = cut2.resize((x,y),Image.ANTIALIAS)
resizeCut3 = cut3.resize((x,y),Image.ANTIALIAS)
resizeCut4 = cut4.resize((x,y),Image.ANTIALIAS)
main = Image.open("main.png")
main.paste(resizeCut1,(0,121))
main.paste(resizeCut2,(0,201))
main.paste(resizeCut3,(0,289))
main.paste(resizeCut4,(0,371))
main.save("mainjng1.png")

